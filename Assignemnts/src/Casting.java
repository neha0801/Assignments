public class Casting {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// create a double value and initialize the value
		double num = 3.14159;
		int castNum =0;
		
		// cast the double value
		castNum = (int) num;
		// print the casted value
		System.out.println("The casted value is " + castNum);
	}

}
