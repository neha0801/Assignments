public class Shortcuts {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		int count =1;
		
		System.out.println("Countdown to 20:");
		// loop runs till the count value is 20
		while(count <=20){
			System.out.println(+count);
			count+=1;	// shortcut used for increment of variable count
		}		
	}
}
